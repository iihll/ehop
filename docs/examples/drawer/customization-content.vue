<template>
  <eh-button text @click="table = true">Open Drawer with nested table</eh-button>
  <eh-button text @click="dialog = true">Open Drawer with nested form</eh-button>
  <eh-drawer v-model="table" title="I have a nested table inside!" direction="rtl" size="50%">
    <eh-table :data="gridData">
      <eh-table-column property="date" label="Date" width="150" />
      <eh-table-column property="name" label="Name" width="200" />
      <eh-table-column property="address" label="Address" />
    </eh-table>
  </eh-drawer>

  <eh-drawer ref="drawerRef" v-model="dialog" title="I have a nested form inside!" :before-close="handleClose"
    direction="ltr" class="demo-drawer">
    <div class="demo-drawer__content">
      <eh-form :model="form">
        <eh-form-item label="Name" :label-width="formLabelWidth">
          <eh-input v-model="form.name" autocomplete="off" />
        </eh-form-item>
        <eh-form-item label="Area" :label-width="formLabelWidth">
          <eh-select v-model="form.region" placeholder="Please select activity area">
            <eh-option label="Area1" value="shanghai" />
            <eh-option label="Area2" value="beijing" />
          </eh-select>
        </eh-form-item>
      </eh-form>
      <div class="demo-drawer__footer">
        <eh-button @click="cancelForm">Cancel</eh-button>
        <eh-button type="primary" :loading="loading" @click="onClick">{{
          loading ? 'Submitting ...' : 'Submit'
        }}</eh-button>
      </div>
    </div>
  </eh-drawer>
</template>

<script lang="ts" setup>
import { reactive, ref } from 'vue'
import { ElDrawer, ElMessageBox } from 'ehop'

const formLabelWidth = '80px'
let timer

const table = ref(false)
const dialog = ref(false)
const loading = ref(false)

const form = reactive({
  name: '',
  region: '',
  date1: '',
  date2: '',
  delivery: false,
  type: [],
  resource: '',
  desc: '',
})

const gridData = [
  {
    date: '2016-05-02',
    name: 'Peter Parker',
    address: 'Queens, New York City',
  },
  {
    date: '2016-05-04',
    name: 'Peter Parker',
    address: 'Queens, New York City',
  },
  {
    date: '2016-05-01',
    name: 'Peter Parker',
    address: 'Queens, New York City',
  },
  {
    date: '2016-05-03',
    name: 'Peter Parker',
    address: 'Queens, New York City',
  },
]

const drawerRef = ref<InstanceType<typeof ElDrawer>>()
const onClick = () => {
  drawerRef.value!.close()
}

const handleClose = (done) => {
  if (loading.value) {
    return
  }
  ElMessageBox.confirm('Do you want to submit?')
    .then(() => {
      loading.value = true
      timer = setTimeout(() => {
        done()
        // 动画关闭需要一定的时间
        setTimeout(() => {
          loading.value = false
        }, 400)
      }, 2000)
    })
    .catch(() => {
      // catch error
    })
}

const cancelForm = () => {
  loading.value = false
  dialog.value = false
  clearTimeout(timer)
}
</script>
